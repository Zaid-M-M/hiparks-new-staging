"use client";
import React, { useRef, useEffect } from "react";
import { motion, useInView, animate } from "framer-motion";

// Helper component for the counting animation
const AnimatedNumber = ({ value, className }) => {
  const ref = useRef(null);
  // Trigger animation once when the element enters the viewport
  const isInView = useInView(ref, { once: true, margin: "-50px" });

  useEffect(() => {
    if (isInView) {
      const node = ref.current;
      // Animate from 0 to the target value
      const controls = animate(0, value, {
        duration: 2, // Animation duration in seconds
        ease: "easeOut",
        onUpdate: (latest) => {
          // Update the text content directly for performance
          if (node) {
            node.textContent = Math.round(latest);
          }
        },
      });

      return () => controls.stop();
    }
  }, [isInView, value]);

  // Render with the passed className to maintain exact styling
  return (
    <span ref={ref} className={className}>
      0
    </span>
  );
};

